  if (k>=10){
    print("At most 5");
  }else if(k==0){
    x=t
  }else if(k==1){ #second line is the orthonormal basis
    x=sqrt(6)*t*(t-1) #*sqrt(5)
  }else if(k==2){
    x=sqrt(30)*t*(t-1)*(2*t-1) #*sqrt(7)
  }else if(k==3){
    x=2*sqrt(21)/3*(15/16*(2*t-1)^4-9/8*(2*t-1)^2+3/16) #sqrt(15/2)
  }else if(k==4){
    x=3*sqrt(5)/2*(7/4*(2*t-1)^5-5/2*(2*t-1)^3+3/4*(2*t-1)) #sqrt(77/10)
  }else if(k==5){
    x=sqrt(330)/5*(105/32*(2*t-1)^6-175/32*(2*t-1)^4+75/32*(2*t-1)^2-5/32) #sqrt(39/5)
  }else if(k==6){
    x=sqrt(546)/6*(99/16*(2*t-1)^7-189/16*(2*t-1)^5+105/16*(2*t-1)^3-15/16*(2*t-1)) #sqrt(55/7)
  }else if(k==7){
    x=2*sqrt(210)/7*(9009/768*(2*t-1)^8-1617/64*(2*t-1)^6+2205/128*(2*t-1)^4-245/64*(2*t-1)^2+35/256) #sqrt(221/28)
  }else if(k==8){
    x=3*sqrt(34)/4*(715/32*(2*t-1)^9-429/8*(2*t-1)^7+693/16*(2*t-1)^5-105/8*(2*t-1)^3+35/32*(2*t-1)) #sqrt(95/12)
  }else if(k==9){
    x=3*sqrt(190)/38*(46189/256*(2*t-1)^10-122265/256*(2*t-1)^8+114114/256*(2*t-1)^6-43890/256*(2*t-1)^4+5985/256*(2*t-1)^2-133/256)
      #sqrt(119/15)
  }
}